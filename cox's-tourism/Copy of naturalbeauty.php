						<h4 style="background-color:#FFFF00">Inani</h4>
							<p>
								It is about 32 km. South of Cox's Bazar and just on the beach, with the sea to the west and a background of steep hills to the east. Inani casts a magic spell on those who step into that dreamland. It is only half an hour's drive from Cox's Bazar and an ideal place for Sea-bathing and picnic.
							</p>
						<h4 style="background-color:#FFFF00">Moheskhali</h4>
							<p>
								An island off the coast of Cox's Bazar. It has an area of 268 square kilometers. Through the centre of the island and along the eastern coast line rises a range of low hills, 300 feet high; but the coast to the west and north is a lowlying treat, fringed by mangrove jungle. In the hills on the coast is built the shrine of Adinath, dedicated to siva. By its side on the same hill is Buddhist Pagoda.
							</p>
							<h4 style="background-color:#FFFF00">Ramu</h4>
							<p>
								This is a typical Buddhist village, about 16 km. from Cox's Bazar, on the main road to Chittagong. There are monasteries, khyangs and pagodas containing images of Buddha in gold, bronze and other metals inilaid with precious stones.
							<p>One of the most interesting of these temples is on the bank of the Baghkhali river. It houses not only interesting relics and Burmes handicrafts but also a large bronze statue of Buddha measuring thirteen feet high and rests on a six feet high pedestal. The wood carving of this khyang is very delicate and refined.
							</p>

							<p>The village has a charm of its own. Weavers ply there trade in open workshops and craftsmen make handmade cigars in their pagoda like houses.

							</p>
					<h4 style="background-color:#FFFF00"> St. Martins Island </h4>
							<p> 
								This small coral island about 10km (6mi) south-west of the southern tip of the mainland is a tropical cliché, with beaches fringed with coconut palms and bountiful marine life. There''s nothing more strenuous to do here than soak up the rays, but it''s a clean and peaceful place without even a mosquito to disrupt your serenity.

 It''s possible to walk around the island in a day because it measures only 8 sq km (3 sq mi), shrinking to about 5 sq km (2 sq mi) during high tide. Most of island''s 5500 inhabitants live primarily from fishing, and between October and April fisher people from neighbouring areas bring their catch to the island''s temporary wholesale market. A ferry leaves Teknaf for St Martin every day and takes around 3 hours.

Getting to St. Martin''s is a three-step program. First you''ll need to fly or bus it down to Cox''s Bazar, and then catch a bus to Teknaf, which is right on the very tip of Bangladesh, sandwiched up against Myanmar. From Teknar, ferries run daily to St. Martin Island. The total distance from Dhaka to the island is 510km (316mi).
						</p>
					<h4 style="background-color:#FFFF00">Sonadia Island </h4>
							<p>
								It is about seven kilometer of Cox's Bazar and about nine square kilometer in area. The western side of the island is sandy and different kinds of shells are found on the beach. Off the northern part of the island, there are beds of window pane oysters. During winter, fisherman set up temporary camps on the island and dry their catches of sea fish.
							</p>
						<h4 style="background-color:#FFFF00">The Aggamedha Kyang, Cox's Bazar</h4>
							<p>
								Equally elaborate in plan, elevation and decoration is the Aggameda Khyang near the entrance to the Cox's Bazar town which nestles at the foot of a hill under heavy cover of a stand of large trees. The main sanctuary-cum-monastery is carried on a series of round timber columns, which apart from accommodating the prayer chamber and an assembly hall, also is the repository of a large of small bronze Buddha images-mostly of Burmese origin-- and some old manuscripts.</p>

<p>Beyond the main khyang to the south there is an elevated wooden pavilion and a smaller brick temple with a timber and corrugated metal root. Apart from bearing an inscription in Burmese over its entrance the temple contains some large stucco and bronze Buddha images.

							</p>
						<h4 style="background-color:#FFFF00">Teknaf </h4>
							<p>
								Southernmost tip of Bangladesh, Teknaf situated on the Naaf river and just at the end of the hilly regions of the district. Mayanmar is on the opposite bank of Naaf river. Wild animals and birds are available but the most interesting thing is a journey on the river. Wide sandy beach in the backdrop of high hills with green forests is an enchanting scene never to be forgotten.</p>

