						  <p> "Foreigners come here, but we cannot offer everything they need. But still they enjoy the beach and of course the warmth of the people here," said Mahbubul Alam Akash, who hires out colourful beach chairs and sun umbrellas. "A large number of Bangladeshis crowd the town now and their number has been increasing over the past years. we will find time to breathe once the official tourism season is over."
							There are no official figures on the numbers of visitors to Cox's Bazar this winter, but hotel owners said the 10,000 rooms available were full almost seven days a week in December and a second rush is expected for the Eid-al Adha festival. "The winter season helps us to go through rest of the year, when the number of travellers drop dramatically along with revenue," said an official at a major hotel.</p> 
						 
						<p>The maximum temperature here in the winter is only about 12 degrees Celsius (53.6 degrees Fahrenheit), but tourists on the seashore enjoy cups of tea, snacks or the very popular green coconut water.					</p>
					<p>
					Miles of golden sands, towering cliffs, surfing waves, rare conch shells, colorful pagodas, Buddhist temples and tribes, delightful sea-food--this is Cox's Bazar, the tourist capital of Bangladesh. Having the world's longest (120 kilometers.) beach sloping gently down to the blue waters of the Bay of Bengal, Cox's Bazar is one of the most attractive tourist sport in the country. </p>

<p>There are also a few very old wooden Buddhist temples at Ramu, a few kilometers from Cox's Bazar, well worth visiting.</p>

<p>Located at a distance of 152 km. south of Chittagong, the leading seaport of Bangladesh, Cox's Bazar is connected both by air and road from Dhaka and Chittagong.</p>

<p>A drive to Teknaf, which is the southernmost tip of the mainland of Bangladesh, is a memorable journey. A day trip to either Moheshkhali or Sonadia, the deltaic islands nestled among the gentle waves of the Bay of Bengal, will also be really interesting.</p>

<p>Other attractions for visitors are conch shell market, tribal handicraft, salt and prawn cultivation.</p>

